     private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblAccount.getModel();

        model.setRowCount(0);
        for (Network network : business.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                for (UserAccount userAccount : enterprise.getUserAccountDirectory().getUserAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = userAccount.getUsername();
                    row[1] = enterprise.getName();
                    row[2] = network.getName();
                    

                    model.addRow(row);
                }
            }
        }
    }

    private void populateNetworkComboBox(){
        cboNetwork.removeAllItems();
        cboEnterprise.removeAllItems();
       
      //  cboEmployee.setEnabled(false);
        for (Network network : business.getNetworkList()){
            cboNetwork.addItem(network);
        }
       
      
    }


     
    private void populateEnterpriseComboBox(Network network){
       
        Network n = network;
        for (Enterprise e:n.getEnterpriseDirectory().getEnterpriseList())
        {
            cboEnterprise.addItem(e);
        }
    }
    
    
