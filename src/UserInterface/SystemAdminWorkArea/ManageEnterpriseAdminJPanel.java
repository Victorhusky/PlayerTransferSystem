    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed

        Enterprise enterprise = (Enterprise) cboEnterprise.getSelectedItem();
        String username = txtUserId.getText();
        String password = String.valueOf(txtPassword.getPassword());
        String name = txtName.getText();
       // System.out.println("Before : "+ business.getMasterUserAccountDirectory().getUserAccountList().size());
        Employee employee = enterprise.getEmployeeDirectory().createEmployee(name);
       
        if(enterprise instanceof Club){
            UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new ClubAdminRole());
        
         if (account == null)
            {      
           
            JOptionPane.showMessageDialog(null, "Please Change the User Account Id, Since the Id in the system should be unique!");
             }
        }
        else if(enterprise instanceof AgentCompany)
        {
        
            UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new AgentAdminRole());
         if (account == null)
            { 
            JOptionPane.showMessageDialog(null, "Please Change the User Account Id, Since the Id in the system should be unique!");
             }
        }
        else if (enterprise instanceof EvaluatingCompany)
        {
              UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new EvaluationAdmin());
         if (account == null)
            {
                
            JOptionPane.showMessageDialog(null, "Please Change the User Account Id, Since the Id in the system should be unique!");
            }
        }
         else if (enterprise instanceof Labor)
        {
              UserAccount account = enterprise.getUserAccountDirectory().createUserAccount(username, password, employee, new LaborStaff());
        
         if (account == null)
            {
                
            JOptionPane.showMessageDialog(null, "Please Change the User Account Id, Since the Id in the system should be unique!");
             }
        }
        
       
        populateTable();

    }//GEN-LAST:event_btnSubmitActionPerformed

