    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String userName = this.txtUserName.getText();
        // Get Password
        char[] passwordCharArray = this.txtPassword.getPassword();
        String password = String.valueOf(passwordCharArray);

        UserAccount userAccount = business.getUserAccountDirectory().authenticateUser(userName, password);

        Network inNetwork = null;
        Enterprise inEnterprise = null;
        Organization inOrganization = null;

        if(userAccount == null){
            //Step2: Go inside each network and check  each enterprise;
            for(Network network: business.getNetworkList()){
                for(Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()){
                    userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                    //System.out.println(network.getName());
                    if(userAccount == null){
                        //step3: check against each organization inside enterprise:
                        for(Organization organization: enterprise.getOrganizationDirectory().getOrganizationList()){
                            userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if(userAccount != null){
                                inNetwork = network;
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                break;
                            }
                        }
                    }
                    else{
                        inEnterprise = enterprise;
                        inNetwork = network;
                        break;
                    }
                    if(inOrganization != null){
                        break;
                    }
                }
                if(inEnterprise != null){
                    break;
                }
            }
        }

        if(userAccount == null){
            JOptionPane.showMessageDialog(null, "Invalid credentials");
            return;
        }
        else{
            //log in system admin

            //System.out.println("I am in system admin");
            CardLayout layout = (CardLayout) container.getLayout();
            container.add("workArea",userAccount.getRole().createWorkArea(container, userAccount, inOrganization, inEnterprise, inNetwork, business));
            layout.next(this.container);
        }

        this.btnLogin.setEnabled(false);
        this.btnLogout.setEnabled(true);
        this.txtPassword.setEnabled(false);
        this.txtUserName.setEnabled(false);
    }//GEN-LAST:event_btnLoginActionPerformed

