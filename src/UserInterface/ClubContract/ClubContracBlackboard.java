    private void ShowTable(){
        //This table only show request about transfer
        DefaultTableModel model = (DefaultTableModel) this.tblRequest.getModel();        
        model.setRowCount(0);       
        for (WorkRequest wr : this.enterprise.getWorkQueue().getWorkRequestList()){
            
            if(wr.getStatus().equalsIgnoreCase("inclubsend")){
                Object[] row = new Object[5];           
                row[0] = wr;
                row[1] = wr.getStatus();
                row[2] = wr.getWorkRequestType().getValue();
                Enterprise c =null;
                for(Network n: business.getNetworkList()){
                 for(Enterprise e: n.getEnterpriseDirectory().getEnterpriseList()){
                    for(Organization o: e.getOrganizationDirectory().getOrganizationList()){
                        for(UserAccount u:o.getUserAccountDirectory().getUserAccountList())
                        {
                            if(u.getUsername().equals(wr.getSender().getUsername()))
                               c=e; 
                        }
                    }
                 }
                }            
                row[3] =c.getName();            
                row[4] = wr.getSender().getUsername();
                model.addRow(row);
            }
            
        }
    }
    
    public void populateBoradcastTable(){
        DefaultTableModel model = (DefaultTableModel) this.tblBoradcast.getModel();        
        model.setRowCount(0);   
        
        for(Message msg : this.network.getBoardcastMessageDirectory().getMessageList()){
            Object[] row = new Object[1];           
            row[0] = msg;
            model.addRow(row);
        }
    }

