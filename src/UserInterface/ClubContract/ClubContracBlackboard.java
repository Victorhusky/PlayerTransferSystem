    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        if (this.txtBoradcastMessage.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Message cannot be empty");
            return;
        }
        Message msg = new Message();
        msg.setMessage(this.txtBoradcastMessage.getText());
        this.network.getBoardcastMessageDirectory().getMessageList().add(msg);
  
        JOptionPane.showMessageDialog(null, "Message send success");
        populateBoradcastTable();
       
    }//GEN-LAST:event_btnSendActionPerformed


